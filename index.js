///////////////////////////////////////////////////////////////////////////VARIAVEIS PROMODIAIS////////////////////////////////////////////////////////////////////////
let heroi = ""
let xp = 1001
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////ESTRUTURA DE ORGANIZAÇÃO DO RANK/////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////FERRO//////////////////////////////////////////////////////////////////////////////////
switch(true){
    case xp <= 1000:
        heroi = ("Ferro")
    break
////////////////////////////////////////////////////////////////////////////////BRONZE/////////////////////////////////////////////////////////////////////////////////
    case xp >= 1001 || xp <= 2000:
        heroi = ("Bronze")
    break
////////////////////////////////////////////////////////////////////////////////PRATA//////////////////////////////////////////////////////////////////////////////////
    case xp >= 2001 || xp <= 5000:
        heroi = ("Prata")
    break
////////////////////////////////////////////////////////////////////////////////OURO///////////////////////////////////////////////////////////////////////////////////
    case xp >= 5001 || xp <= 7000:
        heroi = ("Ouro")
    break
////////////////////////////////////////////////////////////////////////////////PLATINA////////////////////////////////////////////////////////////////////////////////
    case xp >= 7001 || xp <= 8000:
        heroi = ("Platina")
    break
////////////////////////////////////////////////////////////////////////////////ASCENDENTE/////////////////////////////////////////////////////////////////////////////
    case xp >= 8001 || xp <= 9000:
        heroi = ("Ascendente")
    break
////////////////////////////////////////////////////////////////////////////////IMORTAL////////////////////////////////////////////////////////////////////////////////
    case xp >= 9001 || xp >= 10000:
        heroi = ("Imortal")
    break
/////////////////////////////////////////////////////////////////////////////////RARIDADE//////////////////////////////////////////////////////////////////////////////
    default:
        heroi = ("Raridade")
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////MENSAGEM DE RETORNO//////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
console.log("O Herói de nome " + heroi +  " está no nível de " + xp)